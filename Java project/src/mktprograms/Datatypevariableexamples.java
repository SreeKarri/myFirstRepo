package mktprograms;

public class Datatypevariableexamples {

	public static void main(String[] args) {
		
		byte age =22;

		System.out.println(age);

		short no =3214;

		System.out.println(no);

		int bigger_no = 76543;

		System.out.println(bigger_no);

		long elon_musk_salary = 654321890;

		System.out.println(elon_musk_salary);


		float value1 = 3.45f;

		System.out.println(value1);

		double value2 = 3.22221;
		System.out.println(value2);


		char a = 'I';
		System.out.println(a);


		boolean answer = true;
		System.out.println(answer);

		boolean answer1 = false;
		System.out.println(answer1);


		String name = "Sree Karri";
		System.out.println(name);
	} 
}
